package com.atruedev.kmpble

/**
 * Bluetooth SIG assigned company identifiers.
 * See: https://www.bluetooth.com/specifications/assigned-numbers/company-identifiers/
 */
public object CompanyId {
    public const val APPLE: Int = 0x004C
    public const val GOOGLE: Int = 0x00E0
    public const val MICROSOFT: Int = 0x0006
    public const val SAMSUNG: Int = 0x0075
    public const val NORDIC_SEMICONDUCTOR: Int = 0x0059
    public const val TEXAS_INSTRUMENTS: Int = 0x000D
    public const val QUALCOMM: Int = 0x001D
    public const val INTEL: Int = 0x0002
    public const val SONY: Int = 0x012D
    public const val HUAWEI: Int = 0x027D
}
